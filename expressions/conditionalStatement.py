x = 5

print('The value of x is', x)
if x == 5:
    print('x is equal to 5')
if x > 4:
    print('x is greater than 4')
if x >= 5:
    print('x is greater than or equals to 5')
if x < 6: print('x is less than 6')
if x <= 5:
    print('x is less than or equals to 5')
if x != 6:
    print('x is not equals to 6')
